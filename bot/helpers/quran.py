def get_surah_url(reciter_key: str, surah_num: int) -> str:
    r = RECITERS.get(reciter_key)
    if not r:
        return ""
    return r["url"].format(n=surah_num)


def find_surah(name: str):
    """يبحث عن سورة بالاسم. يرجع (رقم, اسم) أو None"""
    name = (name or "").strip().replace("ا", "ا").replace("أ","ا").replace("إ","ا")
    for i, s in enumerate(SURAHS, 1):
        s_norm = s.replace("أ","ا").replace("إ","ا")
        if name == s_norm:
            return i, s
    return None
